#include "drivers\nrf5x_system.h"
#include "drivers\nrf5x_clock.h"

#include "main.h"
/*====================================================================================================*/
/*====================================================================================================*/
void System_Init( void )
{
  Clock_Config();
  GPIO_Config();
}

int main( void )
{
  System_Init();

  while(1) {
    __GPIO_TOG(1);
    __GPIO_TOG(2);
    __GPIO_TOG(3);
    __GPIO_TOG(4);
    __GPIO_TOG(5);
    __GPIO_TOG(6);
    __GPIO_TOG(8);
    delay_ms(80);
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(1, 6);
  nrf_gpio_cfg_output(8);
}
